class Cube{
public:
    Cube(){
        glGenVertexArrays(1, &cubeVAO);
        glGenBuffers(1, &cubeVBO);
        // fill buffer
        glBindBuffer(GL_ARRAY_BUFFER, cubeVBO);
        glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);
        // link vertex attributes
        glBindVertexArray(cubeVAO);
        glEnableVertexAttribArray(0);
        glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)0);
        glEnableVertexAttribArray(1);
        glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(3 * sizeof(float)));
        glEnableVertexAttribArray(2);
        glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(6 * sizeof(float)));
        glBindBuffer(GL_ARRAY_BUFFER, 0);
        glBindVertexArray(0);
    }

    void Draw(Shader shader,glm::mat4 model,glm::mat4 view,glm::mat4 projection,GLuint texture){
        shader.use();
        shader.setMat4("model",model);
        shader.setMat4("view",view);
        shader.setMat4("projection",projection);
        shader.setInt("texture0",0);
        glBindVertexArray(cubeVAO);
        glActiveTexture(GL_TEXTURE0);
        glBindTexture(GL_TEXTURE_2D,texture);
        glDrawArrays(GL_TRIANGLES,0,36);
        glBindVertexArray(0);
    }

    void Draw(Shader shader,glm::mat4 model,GLuint texture){
        shader.use();
        shader.setMat4("model",model);
        shader.setInt("texture0",0);
        glBindVertexArray(cubeVAO);
        glActiveTexture(GL_TEXTURE0);
        glBindTexture(GL_TEXTURE_2D,texture);
        glDrawArrays(GL_TRIANGLES,0,36);
        glBindVertexArray(0);
    }

    void Draw(Shader shader,glm::mat4 model,GLuint texture, GLuint normalMap){
        shader.use();
        shader.setMat4("model",model);
        shader.setInt("texture0",0);
        shader.setInt("normalMap",1);
        glBindVertexArray(cubeVAO);
        glActiveTexture(GL_TEXTURE0);
        glBindTexture(GL_TEXTURE_2D,texture);
        glActiveTexture(GL_TEXTURE1);
        glBindTexture(GL_TEXTURE_2D,normalMap);
        glDrawArrays(GL_TRIANGLES,0,36);
        glBindVertexArray(0);
    }
    void Destroy(){
        glDeleteVertexArrays(1,&cubeVAO);
        glDeleteBuffers(1,&cubeVBO);
    }
private:
    GLuint cubeVAO,cubeVBO;
};

